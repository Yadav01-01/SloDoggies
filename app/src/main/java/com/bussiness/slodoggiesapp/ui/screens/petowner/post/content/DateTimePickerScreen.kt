@SuppressLint("DefaultLocale")
@RequiresApi(Build.VERSION_CODES.O)
@Composable
fun DateTimePickerScreen(
    modifier: Modifier = Modifier,
    textHeading: String,
    onDateTimeSelected: (LocalDateTime) -> Unit = {},
    timeSelect: (String) -> Unit = {},
    dateSelect: (String) -> Unit = {},
    date:String? = null,
    time:String? = null
) {
    val isCalendarVisible = remember { mutableStateOf(false) }
    val currentMonth = remember { mutableStateOf(YearMonth.now()) }

    val selectedDate = remember { mutableStateOf<LocalDate?>(null) }
    val selectedHour = remember { mutableIntStateOf(11) }
    val selectedMinute = remember {mutableIntStateOf(38) }

    val isAM = remember { mutableStateOf(true) }

    Log.d("selectedDate", "****${selectedDate.value}")
    LaunchedEffect(time) {
        // -------- TIME SETTING --------
        if (!time.isNullOrEmpty()) {
            try {
                // Example time: "12:05 AM"
                val parts = time.split(" ")
                val timePart = parts[0]           // "12:05"
                val ampmPart = parts[1]           // "AM" or "PM"
                val hm = timePart.split(":")
                val hour = hm[0].toInt()
                val minute = hm[1].toInt()
                selectedHour.intValue = hour
                selectedMinute.intValue = minute
                // AM / PM Logic
                isAM.value = ampmPart.equals("AM", ignoreCase = true)

            } catch (_: Exception) {}
        }
        // -------- DATE SETTING --------
        if (!date.isNullOrEmpty()) {
            try {
                // Example: "2025-11-28"
                val parsedDate = LocalDate.parse(date)
                selectedDate.value = parsedDate
            } catch (_: Exception) {}
        }
    }


    val showTimePicker = remember { mutableStateOf(false) }
    val context = LocalContext.current

    Column(modifier = modifier
            .fillMaxWidth()
            .wrapContentHeight()
            .background(Color.White)) {
        // Header
        Text(
            text = textHeading,
            style = MaterialTheme.typography.titleLarge.copy(
                fontWeight = FontWeight.Medium,
                fontSize = 14.sp,
                fontFamily = FontFamily(Font(R.font.outfit_medium))
            ),
            color = Color.Black,
            modifier = Modifier.padding(bottom = 4.dp)
        )

        // Selected Date and Time Display
        Box(
            modifier = Modifier
                .fillMaxWidth()
                .height(48.dp)
                .border(1.dp, Color(0xFFAEAEAE), RoundedCornerShape(8.dp))
                .clip(RoundedCornerShape(8.dp))
                .clickable { isCalendarVisible.value = true }
                .padding(horizontal = 12.dp),
            contentAlignment = Alignment.CenterStart
        ) {
            if (selectedDate.value != null) {
                val formatter = DateTimeFormatter.ofPattern("MMMM d, yyyy")
                Text(
                    text = "${selectedDate.value!!.format(formatter)} " +
                            String.format("%02d:%02d", selectedHour.intValue, selectedMinute.intValue) +
                            " ${if (isAM.value) "AM" else "PM"}",
                    fontSize = 14.sp,
                    fontFamily = FontFamily(Font(R.font.outfit_regular)),
                    color = Color.Black
                )
            } else {
                Text(
                    text = "Select Date And Time",
                    fontSize = 14.sp,
                    fontFamily = FontFamily(Font(R.font.outfit_regular)),
                    color = TextGrey
                )
            }
            // Calendar icon on right side
            Image(
                painter = painterResource(id = R.drawable.ic_date_iconn),
                contentDescription = "Calendar",
                modifier = Modifier
                    .size(22.dp)
                    .align(Alignment.CenterEnd)
            )
        }
        // Calendar Section
        if (isCalendarVisible.value) {
            Card(
                modifier = Modifier
                    .fillMaxWidth()
                    .padding(8.dp),
                shape = RoundedCornerShape(16.dp),
                colors = CardDefaults.cardColors(containerColor = Color.White),
                elevation = CardDefaults.cardElevation(defaultElevation = 8.dp)
            ) {
                Box(
                    modifier = Modifier
                        .fillMaxWidth()
                        .clip(RoundedCornerShape(12.dp))
                        .background(Color.White)
                        .padding(13.dp)
                ) {
                    CalendarView(
                        currentMonth = currentMonth.value,
                        selectedDate = selectedDate.value,
                        onDateSelected = { date ->
                            selectedDate.value = date
                            isCalendarVisible.value = false
                            dateSelect(date.toString())
                            if (isAM.value){
                                timeSelect(String.format("%02d:%02d", selectedHour.intValue, selectedMinute.intValue)+" "+"AM")
                            }else{
                                timeSelect(String.format("%02d:%02d", selectedHour.intValue, selectedMinute.intValue)+" "+"PM")
                            }                        },
                        onMonthChanged = { newMonth ->
                            currentMonth.value = newMonth
                        }
                    )
                }
                // Time Selection Section
                Row(
                    modifier = Modifier
                        .fillMaxWidth()
                        .padding(13.dp),
                    verticalAlignment = Alignment.CenterVertically
                ) {
                    Text(
                        text = "Time",
                        fontSize = 20.sp,
                        color = Color.Black,
                        fontFamily = FontFamily(Font(R.font.outfit_regular)),
                        modifier = Modifier
                            .weight(1f)
                            .padding(start = 8.dp)
                    )
                    Row(
                        modifier = Modifier
                            .wrapContentWidth()
                            .wrapContentHeight()
                            .border(
                                width = 1.dp,
                                color = Color(0xFFF2F2F2),
                                shape = RoundedCornerShape(12.dp)
                            ),
                        verticalAlignment = Alignment.CenterVertically
                    ) {
                        // Hour and Minute Display
                        Row(
                            modifier = Modifier
                                .wrapContentWidth()
                                .padding(horizontal = 10.dp),
                            verticalAlignment = Alignment.CenterVertically
                        ) {
                            Text(
                                text = String.format("%02d", selectedHour.intValue),
                                fontSize = 18.sp,
                                fontFamily = FontFamily(Font(R.font.outfit_regular)),
                                color = Color.Black,
                                modifier = Modifier
                                    .clickable { showTimePicker.value = true }
                                    .padding(horizontal = 8.dp)
                            )

                            // Colon
                            Text(
                                text = ":",
                                fontSize = 18.sp,
                                fontFamily = FontFamily(Font(R.font.outfit_regular)),
                                color = Color.Black
                            )

                            // Minute
                            Text(
                                text = String.format("%02d", selectedMinute.intValue),
                                fontSize = 18.sp,
                                fontFamily = FontFamily(Font(R.font.outfit_regular)),
                                color = Color.Black,
                                modifier = Modifier
                                    .clickable { showTimePicker.value = true }
                                    .padding(horizontal = 8.dp)
                            )
                        }

                        Spacer(Modifier.width(12.dp))

                        // AM/PM Column
                        Column(
                            modifier = Modifier
                                .border(
                                    width = 1.dp,
                                    color = Color(0xFFF2F2F2),
                                    shape = RoundedCornerShape(topEnd = 12.dp, bottomEnd = 12.dp)
                                )
                                .clip(RoundedCornerShape(topEnd = 12.dp, bottomEnd = 12.dp))
                                .background(Color.White)
                        ) {
                            // AM
                            Box(
                                modifier = Modifier
                                    .width(60.dp)
                                    .height(32.dp)
                                    .background(if (isAM.value) PrimaryColor else Color.Transparent)
                                    .clickable { isAM.value = true
                                               },
                                contentAlignment = Alignment.Center
                            ) {
                                Text(
                                    text = "AM",
                                    color = if (isAM.value) Color.White else Color.Black,
                                    fontSize = 12.sp,
                                    fontFamily = FontFamily(Font(R.font.outfit_regular))
                                )
                            }

                            // PM
                            Box(
                                modifier = Modifier
                                    .width(60.dp)
                                    .height(32.dp)
                                    .background(if (!isAM.value) PrimaryColor else Color.Transparent)
                                    .clickable { isAM.value = false },
                                contentAlignment = Alignment.Center
                            ) {
                                Text(
                                    text = "PM",
                                    color = if (!isAM.value) Color.White else Color.Black,
                                    fontSize = 12.sp,
                                    fontFamily = FontFamily(Font(R.font.outfit_regular))
                                )
                            }
                        }
                    }
                }
            }
        }

    }

    PopupTimePicker(
        show = showTimePicker.value,
        onDismiss = { showTimePicker.value = false },
        onConfirm = { hour, minute, am ->
            selectedHour.intValue = hour
            selectedMinute.intValue = minute
            isAM.value = am
            if (isAM.value){
                timeSelect(String.format("%02d:%02d", selectedHour.intValue, selectedMinute.intValue)+" "+"AM")
            }else{
                timeSelect(String.format("%02d:%02d", selectedHour.intValue, selectedMinute.intValue)+" "+"PM")
            }
             showTimePicker.value = false
        }
    )


}


@RequiresApi(Build.VERSION_CODES.O)
@Composable
fun CalendarView(
    currentMonth: YearMonth,
    selectedDate: LocalDate?,
    onDateSelected: (LocalDate) -> Unit,
    onMonthChanged: (YearMonth) -> Unit,
    modifier: Modifier = Modifier
) {
    Column(
        modifier = modifier.fillMaxWidth()
    ) {
        MonthHeader(currentMonth, onMonthChanged)
        DayNamesRow()
        WeeksGrid(currentMonth, selectedDate, onDateSelected)
    }
}

